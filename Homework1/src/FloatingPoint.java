public class FloatingPoint {
    public static void main(String[] args) {
//   Խնդիր 6 Ստեղծել 2 փոփոխական(float j և float k ),  վերագրել կամայական արժեքներ և տպել console -ում․
        float j = 234.5f;
        float k = 250.5f;
        System.out.println(j+","+k);
//   Խնդիր 7 Ստեղծել 2 փոփոխական(double l և double m ),վերագրել կամայական արժեքներ և տպել console -ում․
        double l = 11.4d;
        double m = 23.4d;
        System.out.println(l+","+m);
//   Խնդիր 8 Տպել float և double  Max & Min  արժեքները;
        System.out.println("min:" + l +","+ "max:" + k);
//   Խնդիր 9 Հետևյալ արժեքներից ո՞րը կարելի է վերագրել float տիպի փոփոխականին,
//    իսկ ո՞րը double-ին: 34.567839023, 12.345, 8923.1234857,  3456.091?
//    float - 12.345,8923.1234857,3456.091
//    double -  34.567839023, 12.345, 8923.1234857, 3456.091
    }
}
