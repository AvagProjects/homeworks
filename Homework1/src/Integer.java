public class Integer {
    public static void main(String[] args) {
// Խնդիր 1 Ստեղծել 2 փոփոխական(byte a և byte b ),  վերագրել կամայական արժեքներ և տպել   console -ում․
        byte a = 1;
        byte b = 2;
        System.out.println(a+","+b);
// Խնդիր 2 Ստեղծել 2 փոփոխական(short c և short d ),  վերագրել կամայական արժեքներ և տպել console -ում
        short c = 11;
        short d = 22;
        System.out.println(c+","+d);
// Խնդիր 3 Ստեղծել 2 փոփոխական(int e և int f ),  վերագրել կամայական արժեքներ և տպել console -ում
        int e = 202;
        int f = 303;
        System.out.println(e+","+f);
//  Խնդիր 4 Ստեղծել 2 փոփոխական(long g և long h ),  վերագրել կամայական արժեքներ և տպել console -ում
         long g = 55;
         long h = 555;
         System.out.println(g+","+h);
//  Խնդիր 5 Տպել  byte, short, int և long  Max & Min  արժեքները
        System.out.println("MAX:" + a + "," + "MIN:" + h);
//  Խնդիր 6 Ստեղծել 2  փոփոխական (byte և int ) , վերագրել կամայական արժեքներ 2-ական տեսքով  և տպել   console -ում․
        byte i = 0b00001111;
        int j = 0b10101010;
        System.out.println(i + "," + j);
    }
}